export default async function GalleryYearPage({
  params,
}: {
  params: Promise<{ locale: Locale; year: string }>
}) {
  const { locale, year } = await params
  const dict = await getDictionary(locale)

  // Only 2025 is available locally
  if (year !== "2025") {
    notFound()
  }

  return (
    <main className="min-h-screen">
      <Header locale={locale} dict={dict} />
      <div className="pt-24 pb-20">
        <div className="max-w-7xl mx-auto px-4 sm:px-6 lg:px-8">
          <div className="mb-8">
            <Link
              href={`/${locale}/gallery`}
              className="inline-flex items-center gap-2 text-primary hover:text-primary/80 font-medium transition-colors"
            >
              <ArrowLeft className="w-4 h-4" />
              {locale === "mn" ? "Буцах" : "Back to Gallery"}
            </Link>
          </div>

          <div className="text-center mb-12">
            <h1 className="text-3xl sm:text-4xl lg:text-5xl font-bold text-foreground mb-4">
              MEF 2025 {dict.gallery.label}
            </h1>
            <p className="text-lg text-muted-foreground">
              {gallery2025Images.length} {locale === "mn" ? "зураг" : "photos"}
            </p>
          </div>

          <div className="grid grid-cols-2 sm:grid-cols-3 lg:grid-cols-4 gap-4">
            {gallery2025Images.map((image, index) => (
              <a
                key={image}
                href={`/Gallery/2025/${image}`}
                target="_blank"
                rel="noopener noreferrer"
                className="group relative aspect-square overflow-hidden rounded-lg bg-muted"
              >
                <img
                  src={`/Gallery/2025/${image}`}
                  alt={`MEF 2025 - Photo ${index + 1}`}
                  className="w-full h-full object-cover group-hover:scale-105 transition-transform duration-300"
                  loading="lazy"
                />
                <div className="absolute inset-0 bg-black/0 group-hover:bg-black/20 transition-colors" />
              </a>
            ))}
          </div>
        </div>
      </div>
      <Footer dict={dict} locale={locale} />
    </main>
  )
}
