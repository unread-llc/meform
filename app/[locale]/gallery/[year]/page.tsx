const validYears = ["2010", "2011", "2012", "2016", "2018", "2022", "2023", "2024", "2025"]

export default async function GalleryYearPage({
  params,
}: {
  params: Promise<{ locale: Locale; year: string }>
}) {
  const { locale, year } = await params
  const dict = await getDictionary(locale)

  if (!validYears.includes(year)) {
    notFound()
  }

  const images = galleryImages[year] || []

  return (
    <main className="min-h-screen">
      <Header locale={locale} dict={dict} />
      <div className="pt-24 pb-20">
        <div className="max-w-7xl mx-auto px-4 sm:px-6 lg:px-8">
          <div className="mb-8">
            <Link
              href={`/${locale}/gallery`}
              className="inline-flex items-center gap-2 text-primary hover:text-primary/80 font-medium transition-colors"
            >
              <ArrowLeft className="w-4 h-4" />
              {locale === "mn" ? "Буцах" : "Back to Gallery"}
            </Link>
          </div>

          <div className="text-center mb-12">
            <h1 className="text-3xl sm:text-4xl lg:text-5xl font-bold text-foreground mb-4">
              MEF {year} {dict.gallery.label}
            </h1>
            <p className="text-lg text-muted-foreground">
              {images.length} {locale === "mn" ? "зураг" : "photos"}
            </p>
          </div>

          <GalleryGrid images={images} year={year} />
        </div>
      </div>
      <Footer dict={dict} locale={locale} />
    </main>
  )
}
